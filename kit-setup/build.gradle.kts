// ─────────────────────────────────────────────────────────────────────────────
// Template embedding
// ─────────────────────────────────────────────────────────────────────────────
// The CLI must work without runtime resource lookup (Kotlin/Native), so every
// file under templates/ is captured into a generated Kotlin source as string
// constants in a single Map. The result is a normal commonMain source the
// compiler folds into the binary.

val templatesSourceDir = layout.projectDirectory.dir("templates")
val templatesGenRoot = layout.buildDirectory.dir("generated/templates/kotlin")

val generateTemplates by tasks.registering {
    description = "Embeds templates/ files into a generated Templates.kt source."
    group = "build"

    val inputDir = templatesSourceDir
    val outputRoot = templatesGenRoot

    inputs.dir(inputDir).withPropertyName("templates").withPathSensitivity(PathSensitivity.RELATIVE)
    outputs.dir(outputRoot).withPropertyName("templatesOut")

    doLast {
        val baseDir: Path = inputDir.asFile.toPath()
        val files = inputDir.asFileTree.matching { include("**/*") }.files
            .filter { it.isFile }
            .sortedBy { it.absolutePath }

        val pkgDir = outputRoot.get().asFile.resolve("com/aikit/setup/templates")
        pkgDir.mkdirs()
        val outFile = pkgDir.resolve("Templates.kt")

        val sb = StringBuilder()
        sb.appendLine("// Generated by :generateTemplates — do not edit.")
        sb.appendLine("package com.aikit.setup.templates")
        sb.appendLine()
        sb.appendLine("internal object Templates {")
        sb.appendLine("    val files: Map<String, String> = mapOf(")
        for (f in files) {
            val rel = baseDir.relativize(f.toPath()).toString().replace('\\', '/')
            val content = f.readText(Charsets.UTF_8)
            sb.append("        ")
            sb.append(escapeKotlinStringLiteral(rel))
            sb.append(" to ")
            sb.append(emitChunkedKotlinString(content))
            sb.appendLine(",")
        }
        sb.appendLine("    )")
        sb.appendLine("}")
        outFile.writeText(sb.toString(), Charsets.UTF_8)
    }
}

