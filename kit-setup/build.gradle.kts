// ── helpers ──────────────────────────────────────────────────────────────────

fun escapeKotlinStringLiteral(s: String): String {
    val sb = StringBuilder(s.length + 2)
    sb.append('"')
    appendEscapedKotlin(sb, s)
    sb.append('"')
    return sb.toString()
}

/**
 * Emits [content] as a Kotlin expression that produces the original string at
 * compile time. Long strings are split into ~1000-char chunks joined with `+`
 * so we don't rely on any single literal being short enough for the compiler.
 */
fun emitChunkedKotlinString(content: String): String {
    if (content.isEmpty()) return "\"\""
    val chunkSize = 1000
    val sb = StringBuilder()
    var i = 0
    var first = true
    while (i < content.length) {
        val end = (i + chunkSize).coerceAtMost(content.length)
        val piece = content.substring(i, end)
        if (!first) sb.append(" + ")
        sb.append(escapeKotlinStringLiteral(piece))
        first = false
        i = end
    }
    return sb.toString()
}

fun appendEscapedKotlin(sb: StringBuilder, s: String) {
    for (c in s) {
        when (c) {
            '\\' -> sb.append("\\\\")
            '"' -> sb.append("\\\"")
            '\n' -> sb.append("\\n")
            '\r' -> sb.append("\\r")
            '\t' -> sb.append("\\t")
            '$' -> sb.append("\\$")
            else -> if (c.code < 0x20) {
                sb.append("\\u")
                val hex = c.code.toString(16)
                repeat(4 - hex.length) { sb.append('0') }
                sb.append(hex)
            } else {
                sb.append(c)
            }
        }
    }
}
